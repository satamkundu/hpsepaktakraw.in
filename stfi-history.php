				<p>Speak Takraw Is a Traditional Malaysian sport and is popular in south East Asian countries. It is' played in Malaysia, Singapore, Thailand, Philippines, Brunei and Indonesia. Sepak Takraw was introduced in India in 1980 when a group of sports enthusiast in Nagpur obtained printed materials and photographs from the Sepak Takraw Federation of Malaysia with Shri Tajuddin Ahmed as the Secretary General.</p>
				
				<p>During the same period a meeting of sports lovers was held in the office of Nagpur Sharirik Shikshan Mahavidyalaya and Sepak Takraw Federation of india was formed with Adv. S. W. Dhabe, Member of Parliament as its President, Mr. Salim Baig as Secretary General an Mr. Joseph Rao as treasurer. The formation of Federation was on the eve of the Asian Games held in Delhi from Nov. 19 1982 to Dec. 4, 1982, to Dec. 4, 1982. Since the Sepak Takraw was a demonstration sport in Asian Games '82 in Delhi, a Sepak Takraw contingent from the Asian Sepak Takraw Federation (Hq. Kualalumpur) was invited by the spl. Organising Committee of the Asian Games. The contingent comprised of playing members from Malaysia, Philippines, Indonesia, Brunei and India.</p>

				<p>The newly formed Sepak Takraw federation of India was entrusted with the organization of the event at Karnail Singh Stadm in Delhi on Nov. 23 in the afternoon. The demonstration was a big success and team from South East Asian countries along with infant Indian team gave a spectacular show before a capacity crowd in Delhi. The demonstration was telecast live and print media gave a very good coverage, the then Prime Minister of India Mrs. Indira Gandhi who took special interest I organizing the event and including Sepak Takraw as demonstration event was the guest of honour at the demonstration.</p>

				<p>She was introduced to the players and officials by Sepak Takraw federation of India President Mr. S.W. Dhabe, the then M.P. Speaking on the occasion Prime Minister had praised the contribution of Asian Sepak Federation for sending a big contingent of players to make the demonstration a success. She indeed asked to encourage the traditional Malaysian sport of Sepak Takraw in India, so that South East Asian countries playing Sepak Takraw will reciprocate to popularize Indian indigenous sport like Kabaddi, Atya Patya and Kho-Kho in their countries. That was the beginning of the Sepak Takraw I India which got off to a fine start in 1982.</p>

				<p>Immediately after the 1982 demonstration in Delhi During Asian Games at Kamall Singh Stadium, the Federation initiated Demonstration of Sepak Takraw in other parts of the country to seizer the opportunity of availability of the Sepak Takraw contingent from Asian Sepak Takraw Federation which had come for Asian Games demonstration. The Federation organised demonstration at Bombay. Nagpur and in Delhi outside the Asian Games venue. The demonstration in Bombay and Nagpur were organised by the state Assocition under the full . guidance of Federation.</p>

